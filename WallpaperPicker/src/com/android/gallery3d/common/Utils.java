    private static final long POLY64REV = 0x95AC9329AC4BC9B5L;
    private static final long INITIALCRC = 0xFFFFFFFFFFFFFFFFL;

    private static long[] sCrcTable = new long[256];

    /* SPRD: bug375727 2014-12-05 Bugfix avoid OOM when handle in-memory decode. @{ */
    public static final int MAX_BITMAP_SIZE = 25 * 1024 * 1024; // 25M
    public static final int MAX_CHANCE_TO_SCALE_DOWN = 4;
