    public static void waitWithoutInterrupt(Object object) {
        try {
            object.wait();
        } catch (InterruptedException e) {
            Log.w(TAG, "unexpected interrupt: " + object);
        }
    }

    public static boolean handleInterrruptedException(Throwable e) {
        // A helper to deal with the interrupt exception
        // If an interrupt detected, we will setup the bit again.
        if (e instanceof InterruptedIOException
                || e instanceof InterruptedException) {
            Thread.currentThread().interrupt();
            return true;
        }
        return false;
    }

    /**
     * @return String with special XML characters escaped.
     */
    public static String escapeXml(String s) {
        StringBuilder sb = new StringBuilder();
        for (int i = 0, len = s.length(); i < len; ++i) {
            char c = s.charAt(i);
            switch (c) {
                case '<':  sb.append("&lt;"); break;
                case '>':  sb.append("&gt;"); break;
                case '\"': sb.append("&quot;"); break;
                case '\'': sb.append("&#039;"); break;
                case '&':  sb.append("&amp;"); break;
                default: sb.append(c);
            }
        }
        return sb.toString();
    }

    public static String getUserAgent(Context context) {
        PackageInfo packageInfo;
        try {
            packageInfo = context.getPackageManager().getPackageInfo(context.getPackageName(), 0);
        } catch (NameNotFoundException e) {
            throw new IllegalStateException("getPackageInfo failed");
        }
        return String.format("%s/%s; %s/%s/%s/%s; %s/%s/%s",
                packageInfo.packageName,
                packageInfo.versionName,
                Build.BRAND,
                Build.DEVICE,
                Build.MODEL,
                Build.ID,
                Build.VERSION.SDK_INT,
                Build.VERSION.RELEASE,
                Build.VERSION.INCREMENTAL);
    }

    public static String[] copyOf(String[] source, int newSize) {
        String[] result = new String[newSize];
        newSize = Math.min(source.length, newSize);
        System.arraycopy(source, 0, result, 0, newSize);
        return result;
    }

    // Mask information for debugging only. It returns <code>info.toString()</code> directly
    // for debugging build (i.e., 'eng' and 'userdebug') and returns a mask ("****")
    // in release build to protect the information (e.g. for privacy issue).
    public static String maskDebugInfo(Object info) {
        if (info == null) return null;
        String s = info.toString();
        int length = Math.min(s.length(), MASK_STRING.length());
        return IS_DEBUG_BUILD ? s : MASK_STRING.substring(0, length);
    }

    // This method should be ONLY used for debugging.
    public static void debug(String message, Object ... args) {
        Log.v(DEBUG_TAG, String.format(message, args));
    }

    /* SPRD: bug375727 2014-12-05 Bugfix avoid OOM when handle in-memory decode. @{ */
    /**
     * A function that return a proper BitmapFactory.Options to avoid OOM problem
     * when use BitmapFactory to decode picture in-memory.
     * You should check the outWidth and outHeight of the returned object to determine
     * the proper result is returned.
     *
     * @param context
     * @param uri
     * @param filePath
     * @param res
     * @param resId
     * @param initalSampleSize
     * @return
     */
    public static BitmapFactory.Options getPropertyBitmapFactoryOptions(
            Context context, Uri uri, String filePath, Resources res, int resId,
            byte[] imageBytes, int initalSampleSize) {

        int chance = MAX_CHANCE_TO_SCALE_DOWN;
        int sampleSize = Math.max(initalSampleSize, 1);

        InputStream is = null;
        BitmapFactory.Options opts = new BitmapFactory.Options();
        opts.inJustDecodeBounds = true;

        boolean handleUri = (context != null && uri != null);
        boolean handleFile = (filePath != null);
        boolean handleResources = (res != null && resId > 0);
        boolean handleBytes = (imageBytes != null);

        while(chance-- > 0) {
            if (handleUri || handleResources || handleBytes) {
                is = getInputStream(context, uri, res, resId, imageBytes);
                /* SPRD: bug393939 2015-02-02 Bugfix Security exception cause set wallpaper fail. @{ */
                if (is != null) {
                    opts.inSampleSize = sampleSize;
                    BitmapFactory.decodeStream(is, null, opts);
                    Utils.closeSilently(is);
                } else {
                    opts.outWidth = -1;
                    opts.outHeight = -1;
                    break;
                }
                /* SPRD: bug393939 2015-02-02 Bugfix Security exception cause set wallpaper fail. @} */
            } else if (handleFile) {
                BitmapFactory.decodeFile(filePath, opts);
            } else {
                // no need to run empty loop
                break;
            }

            if (opts.outWidth * opts.outHeight * 4 < MAX_BITMAP_SIZE) {
                break;
            }
            sampleSize *= 2;
        }

        // Output width and height will not be change if the original size is too
        // large, here we return null to the caller to indicator no property size
        // found.
        if (opts.outWidth * opts.outHeight * 4 > MAX_BITMAP_SIZE) {
            return null;
        }

        return opts;
    }

    /**
     * Note: it is the caller's responsibility to close the returned InputStream.
     * @param context
     * @param uri
     * @param res
     * @param resId
     * @return
     */
    public static InputStream getInputStream(Context context, Uri uri,
            Resources res, int resId, byte[] imageBytes) {
        InputStream is = null;
        if (context != null && uri != null) {
            /* SPRD: bug393939 2015-02-02 Bugfix Security exception cause set wallpaper fail. @{ */
            try {
                is = context.getContentResolver().openInputStream(uri);
            } catch (FileNotFoundException e) {
                Log.w(TAG, "getInputStream uri file not found, uri: " + uri);
            } catch (SecurityException ex) {
                Log.w(TAG, "security exception cannot read file: " + uri.toString(), ex);
            }
            /* SPRD: bug393939 2015-02-02 Bugfix Security exception cause set wallpaper fail. @} */
        }
        if (res != null && resId > 0) {
            try {
                is = res.openRawResource(resId);
            } catch (NotFoundException e) {
                Log.w(TAG, "getInputStream resources file not found, resId: " + resId);
            }
        }
        if (imageBytes != null) {
            is = new ByteArrayInputStream(imageBytes);
        }
        if (is != null) {
            is = new BufferedInputStream(is);
        }
        return is;
    }
